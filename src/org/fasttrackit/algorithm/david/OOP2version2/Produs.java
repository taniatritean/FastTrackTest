package org.fasttrackit.algorithm.david.OOP2version2;

public class Produs {
    private String name;
    private int price;

    public Produs(String name, int price) {
        this.name = name;
        this.price = price;
    }

    public String getName() {
        return name;
    }

    public int getPrice() {
        return price;
    }

}
